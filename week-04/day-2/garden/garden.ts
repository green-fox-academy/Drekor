'use strict'

import { } from "./garden-APP";
import { Flower } from "./flower";
import { Tree } from "./tree";

export class Garden {

  color: string;
  name: string;
  absorvation: number;
  thirsty: number;
  flowerList: Flower;
  treeList: Tree;

  constructor() {
    this.color;
    this.name = name;
    this.absorvation;
    this.thirsty = 0;
  }

  addFlower(flowerList: Flower) {
    let add;
    flowerList += add.push(flowerList);
  }

  addTree(treeList: Tree) {
    let add;
    treeList += add.push(treeList);
  }
}